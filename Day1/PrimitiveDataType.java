public class PrimitiveDataType {
    static byte n1;
    static short n2;
    static int n3;
    static long n4;
    static float n5;
    static double n6;
    static boolean n7;
    static char n8;

    public static void main(String[] args) {

        System.out.println("Default value of Byte is "+n1);
        System.out.println("Default value of Short is "+n2);
        System.out.println("Default value of Int is "+n3);
        System.out.println("Default value of Long is "+n4);
        System.out.println("Default value of Float is "+n5);
        System.out.println("Default value of Double is "+n6);
        System.out.println("Default value of Boolean is "+n7);
        System.out.println("Default value of Char is "+n8);
    }
}